class WriteDwords(gdb.Command):
    """Write 100 DWORDs (32-bit values) starting from a specified address."""

    def __init__(self):
        super(WriteDwords, self).__init__("write-dwords", gdb.COMMAND_USER)

    def invoke(self, arg, from_tty):
        args = gdb.string_to_argv(arg)
        address = int(args[0], 16)

        for i in range(100):
            m = '{int}'
            gdb.execute(f"set {m}0x{address:x}={hex(vals[i])}")
            address += 4

